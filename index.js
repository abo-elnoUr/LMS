function openPayment() {
    document.getElementById("payment").style.width = "500px";
    document.getElementById("payment").style.opacity = "1";
    document.getElementById("main-section").style.marginRight = "500px";
  }
  
  function closePayment() {
    document.getElementById("payment").style.width = "0";
    document.getElementById("payment").style.opacity = "0";
    document.getElementById("main-section").style.marginRight = "0px";
  }

